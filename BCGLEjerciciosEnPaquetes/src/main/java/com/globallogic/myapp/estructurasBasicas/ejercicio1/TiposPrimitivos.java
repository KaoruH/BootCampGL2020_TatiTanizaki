package com.globallogic.myapp.estructurasBasicas.ejercicio1;

public class TiposPrimitivos {

    public static void main(String[] args) throws Exception {
        
        int myNum = 15;

        System.out.println("El valor de mi numero es " + myNum);

        char myChar = 'a';

        System.out.println("El valor de mi caracter es " + myChar);

        boolean myBoolean = true;

        System.out.println("El valor de b es " + myBoolean);

        float myFloat = 2436.2456F;

        System.out.println("El valor de mi floar es " + myFloat);

        short myShort = 500;

        System.out.println("El valor de mi short es " + myShort);



    }
}